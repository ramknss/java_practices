class Bitwise
{
    public static void main(String[] args)
    {
        int a = 0b11,b = 0b00;
        System.out.println((a&b));
        System.out.println((a|b));
        System.out.println((a^b));
        System.out.println((~a));
        System.out.println((a>>2));
        System.out.println((a<<2));    
    }
}
